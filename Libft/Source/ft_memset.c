#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	void *aux;
	int	i;

	i = 0;
	aux = s;
	while (i < int(n))
	{
		*s = c;
		s++;
		i++;
	}
	return (aux);
}